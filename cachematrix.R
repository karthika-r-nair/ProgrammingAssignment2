##These functions will help to cache the inverse of matrix in order to speed up computation time 

## makeCacheMatrix: This function creates a special "matrix" object that can cache its inverse.
makeCacheMatrix <- function(x = matrix()) {
  
  i<- NULL
  set <- function(y){
    # <<- operator which can be used to assign a value to an object in an environment that is different from the current environment
    x<<- y
    i<<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<-inverse
  getinverse <- function() i
  list(set = set, get = get, setinverse = setinverse, getinverse=getinverse)
}


## cacheSolve: This function computes the inverse of the special "matrix" returned by makeCacheMatrix above. If the inverse has already been calculated (and the matrix has not changed), then the cachesolve should retrieve the inverse from the cache.
cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  i <- x$getinverse()
  if(!is.null(i)){
    message("getting cached data")
    return(i)
  }
  data<- x$get()
  
  i <- solve(data)
  x$setinverse(i)
  message("result")
  i
}
